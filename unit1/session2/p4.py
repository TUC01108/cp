def sum_of_digits(num):
    s = str(num)
    digits = [int(char) for char in s]
    sum_digits = sum(digits)
    # print(sum_digits)
    return sum_digits

# Test cases
num = 423
print(sum_of_digits(num))

num = 4
print(sum_of_digits(num))

